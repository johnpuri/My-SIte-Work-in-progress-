function toggleGridVisibility() {
  var grid = document.getElementById("grid");
  if (grid.style.display === "none") {
    grid.style.display = "block";
  } else {
    grid.style.display = "none";
  }
}

window.addEventListener("DOMContentLoaded", function() {
  var grid = document.getElementById("grid");
  grid.style.display = "none";
});

